package com.example.formulaapi.seasons;

public class Season {
    private String seasonId;
    private String name;
    private String url;
    private int year;

    public String getSeasonId() {
        return seasonId;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public int getYear() {
        return year;
    }
}
